def test_workload_profile_markdown_contains_required_phrases() -> None:
    if not WORKLOAD_PROFILE_MD.exists():
        pytest.skip("outputs/workload_profile.md missing — run the script first.")
    md = WORKLOAD_PROFILE_MD.read_text(encoding="utf-8")
    for section in (
        "Compatible Nonlinear Islands Method",
        "Boundary Call Formulas",
        "Trusted Compute Reduction",
        "Preprocessing Cost Increase",
        "Online Extra Matmul Count",
        "Security Proxy Caveats",
    ):
        assert section in md, f"missing section: {section}"
    for phrase in (
        "ours_compatible_nonlinear_islands is a projected method",
        "Stage 5.2a correctness probes",
        "Stage 5.2b security proxies",
        "not yet integrated into GPT-2 / BERT / T5 wrappers",
        "Compatible mask families are weaker than unrestricted dense masks",
        "Fresh permutation, dense sandwiching, and pad at Linear boundaries are required mitigations",
        "online_extra_matmul_count = 0",
        "not a real TEE measurement",
        # Stage 5.3a / 5.3b / 5.3c — cross-architecture probe-level integration.
        "Stage 5.3a Wrapper Integration Status",
        "GPT-2 model-level integration is available",
        "BERT/T5 are probe-level integrations, not full wrappers",
        # Stage 5.3c value or Stage 6.4-extended value.
        "full_runtime_integrated = False",
        "all_architecture_probe_level_implemented = True",
        "security_profile` remains `proxy-evaluated, not formal`",
    ):
        assert phrase in md, f"missing phrase: {phrase!r}"
    # measured_integration_scope must be present in one of the valid forms.
    assert any(
        phrase in md
        for phrase in (
            "measured_integration_scope = \"cross_architecture_probe_level\"",
            "measured_integration_scope = \"cross_architecture_plus_modern_decoder_probe_level\"",
            "measured_integration_scope = \"cross_architecture_plus_modern_decoder_block_level\"",
            "measured_integration_scope = \"cross_architecture_plus_modern_decoder_model_level\"",
        )
    )
