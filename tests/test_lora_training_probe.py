"""Stage 7.0 — tests for the LoRA training probe."""

from __future__ import annotations

import csv
import json
import subprocess
import sys
from pathlib import Path

import pytest

from pllo.experiments.lora_training_probe import (
    LoRATrainingProbeConfig,
    VALID_OPTIMIZERS,
    normalize_optimizer,
    run_lora_training_probe,
)


PROJECT_ROOT = Path(__file__).resolve().parents[1]
SCRIPT = PROJECT_ROOT / "scripts" / "run_lora_training_experiments.py"


def _cfg(**overrides) -> LoRATrainingProbeConfig:
    base = dict(
        seed=2026, batch_size=4, d_in=16, d_out=8, rank=3,
        num_steps=3, lr=1e-2, optimizer="sgd",
        use_pad=True, fresh_u_per_step=True, fresh_masks_per_step=True,
        dtype="float64",
    )
    base.update(overrides)
    return LoRATrainingProbeConfig(**base)


# ---------------------------------------------------------------------------
# 1. run_lora_training_probe small config runs
# ---------------------------------------------------------------------------


def test_run_lora_training_probe_runs_under_small_config() -> None:
    report = run_lora_training_probe(_cfg())
    assert report["lora_private_training_status"] == "prototype"
    assert (
        report["training_step_correctness"]["num_steps"]
        == _cfg().num_steps
    )


