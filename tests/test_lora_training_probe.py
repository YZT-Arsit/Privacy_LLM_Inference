# ---------------------------------------------------------------------------
# 7. Markdown generated by runner script
# ---------------------------------------------------------------------------


def test_runner_script_generates_json_csv_md(tmp_path: Path) -> None:
    subprocess.run(
        [
            sys.executable, str(SCRIPT),
            "--output-dir", str(tmp_path),
            "--num-steps", "3",
            "--d-in", "16", "--d-out", "8", "--rank", "3",
            "--batch-size", "4",
        ],
        cwd=PROJECT_ROOT, capture_output=True, check=True, text=True,
    )
    md = (tmp_path / "lora_training_experiments.md").read_text(encoding="utf-8")
    for phrase in (
        "Experiment Scope",
        "LoRA Linear Masking Formula",
        "Forward Correctness",
        "Training-Step Correctness",
        "Gradient / Optimizer Handling",
        "Pad Compensation",
        "Limitations",
        "Next Stage Plan",
    ):
        assert phrase in md, f"missing markdown section: {phrase!r}"
    json_text = (tmp_path / "lora_training_experiments.json").read_text(
        encoding="utf-8"
    )
    # No raw tensor literals in JSON.
    assert "tensor(" not in json_text
    with (tmp_path / "lora_training_experiments.csv").open(
        encoding="utf-8"
    ) as f:
        rows = list(csv.DictReader(f))
    assert any(r["section"] == "summary" for r in rows)
    assert any(r["section"] == "per_step" for r in rows)


# ---------------------------------------------------------------------------
# 8. Markdown includes "backward remains trusted" limitation
# ---------------------------------------------------------------------------


def test_markdown_includes_trusted_backward_limitation(tmp_path: Path) -> None:
    subprocess.run(
        [
            sys.executable, str(SCRIPT),
            "--output-dir", str(tmp_path),
            "--num-steps", "2",
            "--d-in", "12", "--d-out", "8", "--rank", "2",
            "--batch-size", "3",
        ],
        cwd=PROJECT_ROOT, capture_output=True, check=True, text=True,
    )
    md = (tmp_path / "lora_training_experiments.md").read_text(encoding="utf-8")
    assert "training backward remains trusted" in md.lower()
    assert "stage 7.0 prototype" in md.lower()
    # Adapter never merged.
    assert "merge_adapter_into_w" in md
    # No PEFT integration claim.
    assert "no peft" in md.lower() or "peft" in md.lower()


